def choice_menu():
    print("You have the following options:  \n")
    print("1. Provision a UniFi device  \n")

def get_user_input(prompt):
    try:
        if hasattr(__builtins__, 'raw_input'):
            return raw_input(prompt)
        else:
            return input(prompt)
    except EOFError:
        return None
    
def choice_switch(choice):
    valid = False

    while valid == False:
        if choice == "1":
            print(" ")
            print("You have chosen to provision a UniFi device")
            valid = True
        else:
            print(" ")
            print("Please enter a valid choice")
            print(" ")
            choice = get_user_input("Please enter your choice: ")

def main():
    os.system("clear")
    print_banner()

    choice_menu()
    choice = get_user_input("Please enter your choice: ")
    choice_switch(choice)

if __name__ == "__main__":
    main()
